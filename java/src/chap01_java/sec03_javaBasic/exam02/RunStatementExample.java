package chap01_java.sec03_javaBasic.exam02;

public class RunStatementExample {

	public static void main(String[] args) {

		int x = 1;					// 변수 x 선언 및 1을 저장
		int y = 2;					// 변수 y 선언 및 2를 저장
		int result = x + y;			// 변수 result 선언 및 x + y 값을 저장
		System.out.println(result); // 모니터에 출력되는 메서드 호출
	}
}